// <<<<<< <<<<<< Swap two numbers using a temporary variable. >>>>>>
console.log(`<<<<<< <<<<<< Swap two numbers using a temporary variable. >>>>>>`);

let a = 10;
let b = 20;

console.log("Before Swap: a = ", a, " Before Swap: b = ", b);

let temp = a;
a = b;
b = temp;

console.log("After Swap: a = ", a, " After Swap: b = ", b);





